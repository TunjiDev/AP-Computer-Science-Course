// PRINT THE FIRST 10 NATURAL NUMBERS USING A FOR LOOP

public class Main {
   public static void main(String[] args) {
      for (int i = 10; i >= 1; i--) {
         System.out.println("The value of i is: " + i);
      }
   }
}

